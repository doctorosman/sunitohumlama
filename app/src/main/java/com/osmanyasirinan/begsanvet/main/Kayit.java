package com.osmanyasirinan.begsanvet.main;

public class Kayit {
    private String ay;
    private String sayi;

    public Kayit(String ay, String sayi) {
        this.ay = ay;
        this.sayi = sayi;
    }

    public String getAy() {
        return ay;
    }

    public void setAy(String ay) {
        this.ay = ay;
    }

    public String getSayi() {
        return sayi;
    }

    public void setSayi(String sayi) {
        this.sayi = sayi;
    }
}
